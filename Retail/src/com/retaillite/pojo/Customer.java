package com.retaillite.pojo;

public class Customer {

	private String customer_name;
	private String customer_number;
	private String customer_email_id;
	private String customer_Oauth;
	private String customer_device_id;

	public String getCustomer_name() {
		return customer_name;
	}

	public void setCustomer_name(String customer_name) {
		this.customer_name = customer_name;
	}

	public String getCustomer_number() {
		return customer_number;
	}

	public void setCustomer_number(String customer_number) {
		this.customer_number = customer_number;
	}

	public String getCustomer_email_id() {
		return customer_email_id;
	}

	public void setCustomer_email_id(String customer_email_id) {
		this.customer_email_id = customer_email_id;
	}

	public String getCustomer_Oauth() {
		return customer_Oauth;
	}

	public void setCustomer_Oauth(String customer_Oauth) {
		this.customer_Oauth = customer_Oauth;
	}

	public String getCustomer_device_id() {
		return customer_device_id;
	}

	public void setCustomer_device_id(String customer_device_id) {
		this.customer_device_id = customer_device_id;
	}

}
